#include "Cube.h"

Cube::Cube(WindowCamera& cam, GLuint programId) : IGameObject(cam, programId)
{
	static const GLfloat g_vertex_buffer_data[] = {
		-1.0f,-1.0f,-1.0f,
		-1.0f,-1.0f, 1.0f,
		-1.0f, 1.0f, 1.0f,
		1.0f, 1.0f,-1.0f,
		-1.0f,-1.0f,-1.0f,
		-1.0f, 1.0f,-1.0f,
		1.0f,-1.0f, 1.0f,
		-1.0f,-1.0f,-1.0f,
		1.0f,-1.0f,-1.0f,
		1.0f, 1.0f,-1.0f,
		1.0f,-1.0f,-1.0f,
		-1.0f,-1.0f,-1.0f,
		-1.0f,-1.0f,-1.0f,
		-1.0f, 1.0f, 1.0f,
		-1.0f, 1.0f,-1.0f,
		1.0f,-1.0f, 1.0f,
		-1.0f,-1.0f, 1.0f,
		-1.0f,-1.0f,-1.0f,
		-1.0f, 1.0f, 1.0f,
		-1.0f,-1.0f, 1.0f,
		1.0f,-1.0f, 1.0f,
		1.0f, 1.0f, 1.0f,
		1.0f,-1.0f,-1.0f,
		1.0f, 1.0f,-1.0f,
		1.0f,-1.0f,-1.0f,
		1.0f, 1.0f, 1.0f,
		1.0f,-1.0f, 1.0f,
		1.0f, 1.0f, 1.0f,
		1.0f, 1.0f,-1.0f,
		-1.0f, 1.0f,-1.0f,
		1.0f, 1.0f, 1.0f,
		-1.0f, 1.0f,-1.0f,
		-1.0f, 1.0f, 1.0f,
		1.0f, 1.0f, 1.0f,
		-1.0f, 1.0f, 1.0f,
		1.0f,-1.0f, 1.0f
	};

	FillBuffer("vertexbuffer", g_vertex_buffer_data, sizeof(g_vertex_buffer_data));
	SetupShaderAttribute("vertexbuffer", 3, GL_FLOAT);
	size = 12 * 3;
	SetColor(1, 1, 1);


	GetUniform("MVP");
	GetUniform("inColor");
}

void Cube::Draw()
{
	glUseProgram(shaderID);
	glBindVertexArray(GetID("vertexarray"));
	glBindBuffer(GL_ARRAY_BUFFER, GetID("vertexbuffer"));
	glUseProgram(shaderID);

	//send shader uniform data
	glUniformMatrix4fv(GetID("MVP"), 1, GL_FALSE, &GetMVP()[0][0]);
	glUniform3f(GetID("inColor"), GetColor().r, GetColor().g, GetColor().b);

	glDrawArrays(GL_TRIANGLES, 0, size);
}
